function prepareRow(value) {
    var rowDiv = document.createElement("div");
    var menuDiv = document.createElement("div");
    menuDiv.classList = menuDiv.classList + ' inline-flex';
    var rollDiv = new Icon(value, "fa_div", "roll_icon", "_minus", "fa-minus");
    var sortUpDiv = new Icon(value, "fa_div", "arrow_up_icon", "_arrow_up", "fa-arrow-up");
    var sortDownDiv = new Icon(value, "fa_div", "arrow_down_icon", "_arrow_down", "fa-arrow-down");
    var moveDiv = new Icon(value, "fa_div", "edit_icon", "_edit", "fa-edit");
    moveDiv.setAttribute("data-toggle", "modal");
    moveDiv.setAttribute("data-target", "#editItem");
    var delDiv = new Icon(value, "fa_div", "times_icon", "_times", "fa-times", "form");
    $(delDiv).append("<input name='rootId' hidden=true value = " + tree_id + " />");
    var addDiv = new Icon(value, "fa_div", "add_icon", "_add", "fa-plus-square");
    addDiv.setAttribute("data-toggle", "modal");
    addDiv.setAttribute("data-target", "#addItem");
    var liNode = document.createElement("LI");
    var textId = document.createElement("INPUT");
    var textValue = document.createElement("INPUT");
    textValue.classList = textValue.classList + ' form-control' + ' tree_value';
    var textParent = document.createElement("INPUT");
    textId.value = value.id;
    textValue.value = value.value;
    menuDiv.appendChild(sortUpDiv);
    menuDiv.appendChild(sortDownDiv);
    menuDiv.appendChild(rollDiv);
    menuDiv.appendChild(textId).hidden = true;
    menuDiv.appendChild(textValue);
    menuDiv.appendChild(textParent).hidden = true;
    menuDiv.appendChild(moveDiv);
    menuDiv.appendChild(addDiv);
    menuDiv.appendChild(delDiv);
    liNode.appendChild(menuDiv);
    liNode.id = value.id;
    rowDiv.appendChild(liNode);
    return rowDiv;
};
